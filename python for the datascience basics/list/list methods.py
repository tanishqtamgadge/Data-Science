list = [2,4,78,43,23]


print(list)

#to append
print(list.append(6))
print(list)

#to sort
print(list.sort())
print(list)

#sort in the reverse
print(list.sort(reverse=True))
print(list)

#in the reverse
print(list.reverse())
print(list)

#to insert in the list
print(list.insert(2,54))
print(list)

#to remove the number in the list
print(list.remove(2))
print(list)

#to remove the number at that index
print(list.pop(4))
print(list)